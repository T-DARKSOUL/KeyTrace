import os
import platform
from pathlib import Path

def run(args):
    keylog = os.environ.get("SSLKEYLOGFILE", "Not set")
    exists = Path(keylog).exists() if keylog != "Not set" else False
    size   = Path(keylog).stat().st_size if exists else 0
    return f"""
Current Configuration
???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
  SSLKEYLOGFILE : {keylog}
  File exists   : {"Yes" if exists else "No"}
  File size     : {f"{size:,} bytes" if exists else "---"}
  OS            : {platform.system()} {platform.release()}
  Python        : {platform.python_version()}
???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
"""
